package java_multithreading.level_2.snake;

/**
 * @author Ivan Korol on 7/19/2018
 */
public class Mouse {
    private int x;
    private int y;

    public Mouse(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
